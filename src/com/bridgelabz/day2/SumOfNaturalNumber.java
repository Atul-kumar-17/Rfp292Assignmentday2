package com.bridgelabz.day2;

public class SumOfNaturalNumber {
    public static void main(String[] args) {
        int n=100;
        int sum=0;
        int i =1;
        while (i<=n){
            sum = sum +i;
            i++;

        }
        System.out.println("Sum of 100 natural numbers = "+sum);

    }
}
